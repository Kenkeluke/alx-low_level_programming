/**
 *_islower - entry point
 *Description: This function checks if a character is lowercase
 *@c: int c represents the parameter for the fnction
 *
 *
 *Return: 0, always success
 *
 *
 */



int _islower(int c)
{
if (c >= 97 && c <= 122)
{
return (1);
}
else
{
return (0);
}

return (0);
}
